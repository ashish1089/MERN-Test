"use client";
import Head from "next/head";
import Link from "next/link";
import { useState } from "react";
import { api } from "~/utils/api";
import { useRouter } from "next/router";

interface UserInput {
  username: string;
  email: string;
  password: string;
}
export default function Home() {
  const router = useRouter();
  const [input, setInput] = useState<UserInput>({
    username: "",
    email: "",
    password: "",
  });

  const { mutate: signUp, data } = api.user.create.useMutation({
    onError: async () => {
      await router.push("/login");
    },
    onSuccess: async () => {
      console.log("signUp Successfully");
      await router.push(
        {
          pathname: "verify",
          query: {
            email: input.email,
          },
        },
        "/verify",
      );
    },
  });
  const handleChange = (event: React.ChangeEvent<HTMLInputElement>) => {
    setInput({ ...input, [event.target.name]: event.target.value });
  };

  return (
    <>
      <Head>
        <title>Create T3 App</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className="mx-auto h-max  max-w-[1440px]">
        <div className="mx-auto mt-8 h-[691px] w-[576px] rounded-[20px] border-2 px-[60px] pt-8">
          <form action="post" onSubmit={async () => signUp(input)}>
            <h1 className="pb-6 text-center text-[32px] font-semibold">
              Create your account
            </h1>
            <label htmlFor="username">
              Name
              <input
                type="text"
                value={input.username}
                onChange={handleChange}
                name="username"
                placeholder="Enter"
                className="mb-7 mt-2 w-full rounded-[6px] border px-4 py-3 "
              />
            </label>
            <label htmlFor="email">
              Email
              <input
                type="email"
                value={input.email}
                onChange={handleChange}
                name="email"
                placeholder="Enter"
                className="mb-7 mt-2 w-full rounded-[6px] border px-4 py-3 "
              />
            </label>
            <label htmlFor="password">
              Password
              <input
                type="password"
                value={input.password}
                onChange={handleChange}
                name="password"
                placeholder="Enter"
                className="mb-7 mt-2 w-full rounded-[6px] border px-4 py-3 "
              />
            </label>

            <button
              className="mt-4 h-[56px] w-[456px] rounded-[6px] bg-black text-white"
              type="submit"
            >
              CREATE ACCOUNT
            </button>
          </form>

          <p className="my-10 text-center text-[#333]">
            Have an Account?
            <span className="ml-3 font-medium">
              <Link href="/login">LOGIN</Link>
            </span>
          </p>
        </div>
      </main>
    </>
  );
}
